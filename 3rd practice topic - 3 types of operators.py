#-----------------------------------------------------------------------------------------
#Comparison Operators:
#operator returns result in boolean.
print (3>2) #is greater than
print (3<2) #is less than
print (3>=2) #is greater than or equal
print (3<=2) #is less than or equal
print (3==2) # is equal to
print (3!=2) # not equal to

#-----------------------------------------------------------------------------------------
# Logical Operators:

print (2>3 or 2>1) # or operator retunrs true if any one of the condition is true
print (3>2 and 2>1) # or operator retunrs true only if both of the condition is true
print (not 2>3 ) # not operator returns reverse value of the condition

